public class prac_if1 {
    public static void main(String[] args) {
        int num1=10;
        int num2=15;

        if (num1>num2) {
            System.out.println("첫 번째 순서가 더 큽니다.");
        }
        else if (num1<num2) {
            System.out.println("두 번째 순서가 더 큽니다");
        }
        else {
            System.out.println("두 숫자는 같습니다");
        }
    }
        }
